#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				a;

	a = 0;
	i = 0;
	while ((s1[i] || s2[i]) && a == 0 && i < n)
	{
		if (s1[i] != s2[i])
		{
			a = s1[i] - s2[i];
		}
		i++;
	}
	return (a);
}
